会員は、当社が本カードの発行、審査、途上与信、利用代金の回収、および本サービスの提供を目的として、会員の氏名、生年月日、住所、電話番号、勤務先、資産状況、および本カードの利用履歴（以下「個人情報」という）を収集、保有、および利用することに同意するものとします。

